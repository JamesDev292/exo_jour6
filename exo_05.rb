# #{} sert à faire de l'interpolation de chaines, on, peut y mettre des variable ou des integer par exemple. 

puts "On va compter le nombre d'heures de travail à THP"
puts "Travail : #{10 * 5 * 11}"   # Calcul la somme des valeurs renseigné
puts "En minutes ça fait : #{10 * 5 * 11 * 60}" #Idem 

puts "Et en secondes ?"  #Chaine de caractère 

puts 10 * 5 * 11 * 60 * 60 # Calcul la somme

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

puts 3 + 2 < 5 - 7      # Vrai ou faux aux chiffres données

puts "Ça fait combien 3 + 2 ? #{3 + 2}"     # Calcul la somme
puts "Ça fait combien 5 - 7 ? #{5 - 7}"     # Calcul la somme

puts "Ok, c'est faux alors !"       #Chaine de caractère

puts "C'est drôle ça, faisons-en plus :"        #Chaine de caractère

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"       # Vrai ou faux aux chiffres données
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}" 
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"